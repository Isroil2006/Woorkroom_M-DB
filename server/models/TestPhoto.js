const mongoose = require("mongoose");

const testPhotoSchema = new mongoose.Schema({
  fileData: {
    type: String,
    required: true,
  },
  uploadedBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("TestPhoto", testPhotoSchema);
